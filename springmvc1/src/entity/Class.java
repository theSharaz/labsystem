package entity;

public class Class {
	
	int classno;
	String name;
	
	public Class() {
		super();

	}
	
	public Class(int classno, String name) {
		super();
		this.classno = classno;
		this.name = name;
	}
	
	public int getClassno() {
		return classno;
	}
	public void setClassno(int classno) {
		this.classno = classno;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
	

}
